module.exports = function(err) {
	console.log('! - - - ERROR FOUND - - - !'.red)
	console.log('LINE: '.cyan + (err.loc.line).toString().yellow)
	console.log('COLUMN: '.cyan + (err.loc.column).toString().yellow)
	console.log('FILE: '.cyan + err.fileName.substring(err.fileName.indexOf('src/')).yellow)
    console.log('MESSAGE: '.cyan + (err.name).yellow)
	console.log('! - - - - - - - - - - - - !'.red)
}